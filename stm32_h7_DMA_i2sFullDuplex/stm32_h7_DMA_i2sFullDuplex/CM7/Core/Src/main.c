// length of delay in seconds
const float    delay_len_seconds = 1.0;

// length of delay in samples
uint32_t  delay_len_samples = (uint32_t) delay_len_seconds * 48000L;

// wet mix (from delay line)
float    delay_wet_mix = 0.5;

